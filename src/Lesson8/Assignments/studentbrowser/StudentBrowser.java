    private void modbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modbtnActionPerformed
        // TODO add your handling code here:
        StudentPopup form = new StudentPopup(this,true);
        form.setForm(s[index]);//put student info in
        form.setModal(true);
        form.setLocationRelativeTo(this);
        form.setVisible(true);
        
        String em = form.getStudent().validateData();
        
        if(em==null){
            s[index]=form.getStudent();
            showStudent();
        }
        else JOptionPane.showMessageDialog(this,em);
        
        
    }//GEN-LAST:event_modbtnActionPerformed

    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        StudentPopup form = new StudentPopup(this,true);
        form.setModal(true);//gives priority & control to popup until dismissed
        form.setLocationRelativeTo(this);//pop up right over the form
        form.setVisible(true);
        //after dispose :
        Student temp = form.getStudent();
        String em = temp.validateData();
        
        if(em==null){
            s[size]=temp;
            index=size;
            size++;
            showStudent();
        }
        else JOptionPane.showMessageDialog(this,em);
    }//GEN-LAST:event_addbtnActionPerformed

    private void btnnextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnextActionPerformed
        // TODO add your handling code here:
        index++;
        if(index==size)
            index--;
        showStudent();
    }//GEN-LAST:event_btnnextActionPerformed

