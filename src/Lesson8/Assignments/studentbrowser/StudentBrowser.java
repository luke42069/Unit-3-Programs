    public void fillData(){
        String name[] = {"Bart Simpson","Swaggy P","Chief Keef","Slatt Slatt","Man Dem"};
        int marks[][] = {
            {58,65,52},{69,69,69},{16,90,32},{99,99,100},{15,10,5}  
        };
        for (int i = 0; i < name.length; i++) {
            s[i] = new Student(name[i],marks[i]);//marks[i] is an array of 3 marks
        }
        size=5;//logical size
        index=0;
    }
    
    public void showStudent(){
        //show 1 student on the form
        txtname.setText(s[index].getName());
        t1lbl.setText(""+s[index].getMark(1));
        t2lbl.setText(""+s[index].getMark(2));
        t3lbl.setText(""+s[index].getMark(3));
        avglbl.setText(""+s[index].getAverage());
        //update bottom labels
        countlbl.setText(""+size);
        indexlbl.setText(""+(index+1));
    }

